
# Lesson 7: Solution

numbers = [1, 5, 2, 8, 4]
print("Original list:", numbers)

numbers.append(10)
print("After append(10):", numbers)

numbers.insert(2, 3)
print("After insert(2, 3):", numbers)

numbers.remove(5)
print("After remove(5):", numbers)

numbers.pop()
print("After pop():", numbers)

numbers.sort()
print("After sort():", numbers)
